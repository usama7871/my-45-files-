// Declare a constant variable named `famous_person` of type `string` and initialize it with the value "Albert Einstein".
const famous_person: string = "Albert Einstein";

// Declare a constant variable named `quote` of type `string` and initialize it with the value "A person who never made a mistake never tried anything new.".
const quote: string = "A person who never made a mistake never tried anything new.";

// Use string interpolation to create a message that combines the `famous_person` and `quote` variables.
