// Define the function make_shirt
function make_shirt(size: string, message: string): void {
    // Print a sentence summarizing the size of the shirt and the message printed on it
    console.log(`The shirt size is ${size} and it has the message: "${message}" printed on it.`);
}

// Call the function with example arguments
make_shirt("L", "Keep calm and code on");
make_shirt("M", "TypeScript is awesome!");
make_shirt("S", "Hello, world!");
